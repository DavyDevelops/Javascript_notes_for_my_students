/**
 * Comparison Operators are used to compare two values
We can compare if one number is greater than, greater than or equals too, less down, and less down or equals too
Note comparison operators returns a boolean true or false
 */

let num1 = 30

let num2 = 60

console.log(num1>num2); //greater than 

console.log(num1<num2); // less than

console.log(num1>=num2); // greater than or equals too

console.log(num1<=num2); // less than or equals too

//operator preceedence: in JS during the course of an operation some operators carry out their function before others. Link to documentation https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Operators/Operator_Precedence